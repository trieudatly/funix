public class ArrayCalculator {
    public static int sumOfArray(int arr[]) {
        int sum = 0;
        for (int item : arr) {
            sum += item;
        }
        return sum;
    }

    public static double sumOfArray(double arr[]) {
        double sum = 0;
        for (double item : arr
        ) {
            sum += item;
        }
        return sum;
    }
}
